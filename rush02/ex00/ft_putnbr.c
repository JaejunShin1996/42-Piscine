#include "ft_rush02.h"

void	ft_putnbr(int nb)
{
	unsigned int	i;

	i = nb;
	if (nb < 0)
	{
		ft_putchar('-');
		i = nb * -1;
	}
	if (9 < i)
	{
		ft_putnbr(i / 10);
	}
	ft_putchar(i % 10 + '0');
}
